package datastruct;

/** 基本类.
 * @author Ljnanest
 */
public class User {
    /** 定义变量.
     * 用户名
     */
    private String username = new String();

    /** 定义变量.
     * 密码
     */
    private String password = new String();

    /** 定义变量.
     * 用户ID
     */
    private String id = new String();

    /** 定义变量.
     * 是否订阅info
     */
    private String info = new String();

    /** 定义变量.
     * 是否订阅系内组织公告
     */
    private String cst = new String();

    /** 定义变量.
     * 是否订阅网络学堂
     */
    private String learn = new String();

    /** 定义变量.
     * 是否订阅人人
     */
    private String renren = new String();

    /** 定义变量.
     * 网络学堂账号
     */
    private String learnna = new String();

    /** 定义变量.
     * 网络学堂密码
     */
    private String learnpa = new String();

    /** 定义变量.
     * 人人账号
     */
    private String renrenna = new String();

    /** 定义变量.
     * 人人密码
     */
    private String renrenpa = new String();

    /** 获取函数.
     * 获取username
     * @return username
     */
    public final String getUsername() {
        return username;
    }

    /** 设置函数.
     * 设置username
     * @param nusername String
     */
    public final void setUsername(final String nusername) {
        this.username = nusername;
    }

    /** 获取函数.
     * 获取password
     * @return password
     */
    public final String getPassword() {
        return password;
    }

    /** 设置函数.
     * 设置password
     * @param npassword String
     */
    public final void setPassword(final String npassword) {
        this.password = npassword;
    }

    /** 获取函数.
     * 获取id
     * @return id
     */
    public final String getId() {
        return id;
    }

    /** 设置函数.
     * 设置id
     * @param nid String
     */
    public final void setId(final String nid) {
        this.id = nid;
    }

    /** 获取函数.
     * 获取info
     * @return info
     */
    public final String getInfo() {
        return info;
    }

    /** 设置函数.
     * 设置username
     * @param ninfo String
     */
    public final void setInfo(final String ninfo) {
        this.info = ninfo;
    }

    /** 获取函数.
     * 获取cst
     * @return cst
     */
    public final String getCst() {
        return cst;
    }

    /** 设置函数.
     * 设置cst
     * @param ncst String
     */
    public final void setCst(final String ncst) {
        this.cst = ncst;
    }

    /** 获取函数.
     * 获取learn
     * @return learn
     */
    public final String getLearn() {
        return learn;
    }

    /** 设置函数.
     * 设置learn
     * @param nlearn String
     */
    public final void setLearn(final String nlearn) {
        this.learn = nlearn;
    }

    /** 获取函数.
     * 获取renren
     * @return renren
     */
    public final String getRenren() {
        return renren;
    }

    /** 设置函数.
     * 设置renren
     * @param nrenren String
     */
    public final void setRenren(final String nrenren) {
        this.renren = nrenren;
    }

    /** 获取函数.
     * 获取learnna
     * @return learnna
     */
    public final String getLearnna() {
        return learnna;
    }

    /** 设置函数.
     * 设置learnna
     * @param nlearnna String
     */
    public final void setLearnna(final String nlearnna) {
        this.learnna = nlearnna;
    }

    /** 获取函数.
     * 获取learnpa
     * @return learnpa
     */
    public final String getLearnpa() {
        return learnpa;
    }

    /** 设置函数.
     * 设置learnpa
     * @param nlearnpa String
     */
    public final void setLearnpa(final String nlearnpa) {
        this.learnpa = nlearnpa;
    }

    /** 获取函数.
     * 获取renrenna
     * @return renrenna
     */
    public final String getRenrenna() {
        return renrenna;
    }

    /** 设置函数.
     * 设置renrenna
     * @param nrenrenna String
     */
    public final void setRenrenna(final String nrenrenna) {
        this.renrenna = nrenrenna;
    }

    /** 获取函数.
     * 获取renrenpa
     * @return renrenpa
     */
    public final String getRenrenpa() {
        return renrenpa;
    }

    /** 设置函数.
     * 设置renrenpa
     * @param nrenrenpa String
     */
    public final void setRenrenpa(final String nrenrenpa) {
        this.renrenpa = nrenrenpa;
    }
}
